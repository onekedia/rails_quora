require 'test_helper'

class UserTopicsControllerTest < ActionDispatch::IntegrationTest
  setup do
    @user_topic = user_topics(:one)
  end

  test "should get index" do
    get user_topics_url
    assert_response :success
  end

  test "should get new" do
    get new_user_topic_url
    assert_response :success
  end

  test "should create user_topic" do
    assert_difference('UserTopic.count') do
      post user_topics_url, params: { user_topic: { topic_id: @user_topic.topic_id, user_id: @user_topic.user_id } }
    end

    assert_redirected_to user_topic_url(UserTopic.last)
  end

  test "should show user_topic" do
    get user_topic_url(@user_topic)
    assert_response :success
  end

  test "should get edit" do
    get edit_user_topic_url(@user_topic)
    assert_response :success
  end

  test "should update user_topic" do
    patch user_topic_url(@user_topic), params: { user_topic: { topic_id: @user_topic.topic_id, user_id: @user_topic.user_id } }
    assert_redirected_to user_topic_url(@user_topic)
  end

  test "should destroy user_topic" do
    assert_difference('UserTopic.count', -1) do
      delete user_topic_url(@user_topic)
    end

    assert_redirected_to user_topics_url
  end
end
